class ForExample
{
	public static void main(String [] args)
	{
		for( char ch = 'A' ; ch <= 'Z';ch++)
		{
			System.out.print(ch+" ");
		}
		
		System.out.println();
		System.out.println("------------------------------");
		
		for( char ch = 97 ; ch <= 122;ch++)
		{
			System.out.print(ch+" ");
		}

		System.out.println();
		System.out.println("------------------------------");
		
		
		for( char ch = 48 ; ch <= 57;ch++)
		{
			System.out.print(ch+" ");
		}
		
		System.out.println();
		System.out.println("------------------------------");
		
		
		for(int i = 0; i <= 100; i++ )
		{
			System.out.print(i+" ");
		}
		
		System.out.println();
		System.out.println("------------------------------");
		
		
		for(int i = 100; i >= 1; i-- )
		{
			System.out.print(i+" ");
		}




	}
}